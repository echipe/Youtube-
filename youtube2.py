def is_music_video(title, duration):
    t = title.lower()

    if duration <= 60:
        return False

    if duration > 600:
        return False

    exclude = ["interview", "対談", "making", "behind", "teaser", "trailer"]
    if any(word in t for word in exclude):
        return False

    include = ["mv", "music video", "official"]
    if any(word in t for word in include):
        return True

    if 120 <= duration <= 480:
        return True

    return False


