def get_uploads_playlist_id(channel_id):
    url = "https://www.googleapis.com/youtube/v3/channels"
    return requests.get(url, params={
        "part": "contentDetails",
        "id": channel_id,
        "key": API_KEY
    }).json()["items"][0]["contentDetails"]["relatedPlaylists"]["uploads"]


def get_all_videos(playlist_id):
    videos = []
    token = None

    while True:
        res = requests.get(
            "https://www.googleapis.com/youtube/v3/playlistItems",
            params={
                "part": "snippet,contentDetails",
                "playlistId": playlist_id,
                "maxResults": 50,
                "pageToken": token,
                "key": API_KEY
            }
        ).json()

        for item in res["items"]:
            videos.append({
                "videoId": item["contentDetails"]["videoId"],
                "title": item["snippet"]["title"],
                "publishedAt": item["contentDetails"]["videoPublishedAt"]
            })

        token = res.get("nextPageToken")
        if not token:
            break

        time.sleep(0.1)

    return videos


def get_video_details(ids):
    return requests.get(
        "https://www.googleapis.com/youtube/v3/videos",
        params={
            "part": "contentDetails,statistics",
            "id": ",".join(ids),
            "key": API_KEY
        }
    ).json()


# ---------------------------
# データ収集
# ---------------------------
def collect_data():
    playlist_id = get_uploads_playlist_id(CHANNEL_ID)
    videos = get_all_videos(playlist_id)

    rows = []

    for i in range(0, len(videos), 50):
        batch = videos[i:i+50]
        ids = [v["videoId"] for v in batch]

        details = get_video_details(ids)

        for item in details["items"]:
            vid = item["id"]
            duration = parse_duration(item["contentDetails"]["duration"])
            meta = next(v for v in batch if v["videoId"] == vid)

            if not is_music_video(meta["title"], duration):
                continue

            stats = item.get("statistics", {})
            views = int(stats.get("viewCount", 0))
            likes = int(stats.get("likeCount", 0))

            rows.append({
                "title": meta["title"],
                "duration_sec": duration,
                "views": views,
                "likes": likes,
                "publishedAt": meta["publishedAt"]
            })

        time.sleep(0.1)

    df = pd.DataFrame(rows)

    # ---------------------------
    # 補正（重要）
    # ---------------------------
    df["publishedAt"] = pd.to_datetime(df["publishedAt"], utc=True)
    now = datetime.now(timezone.utc)

    df["days"] = (now - df["publishedAt"]).dt.days + 1
    df["views_per_day"] = df["views"] / df["days"]

    return df


